//=====================================================================
// 行の読み取り処理
//=====================================================================
int ReadLine(FILE* pFile, char* pBuffer)
{
	char aStrLine[MAX_READABLE_CHAR] = {};

	if (fgets(&aStrLine[0], sizeof(aStrLine), pFile) == NULL)
	{
		return EOF;
	}

	sscanf(&aStrLine[0], "%[^\n]", pBuffer);

	if (strchr(&aStrLine[0], '#') != NULL)
	{
		sscanf(&aStrLine[0], "%[^#]", pBuffer);
	}

	return 0;
}